export const SCHOOL_PROFILE_BY_KEY: Record<string, SchoolProfile> =
	SCHOOL_DIRECTORY.reduce((acc, p) => {
		if (!acc[p.matchKey]) acc[p.matchKey] = p
		return acc
	}, {} as Record<string, SchoolProfile>)

// Chuẩn hóa tên trường -> khóa so khớp (đồng bộ với matchKey đã sinh).
export function normalizeSchoolName(name: string): string {
	let s = name.replace(/đ/g, "d").replace(/Đ/g, "D")
	s = s.normalize("NFD").replace(/[\u0300-\u036f]/g, "")
	s = s.toLowerCase()
	s = s.replace(/truong/g, " ").replace(/dai hoc/g, " ").replace(/ dh /g, " ")
	s = s.replace(/^dh /, " ")
	s = s.replace(/hoc vien/g, " ").replace(/cao dang/g, " ")
	s = s.replace(/[^a-z0-9]+/g, " ").trim()
	s = s.replace(/\s+/g, " ")
	return s
}

export function findSchoolProfile(name: string): SchoolProfile | undefined {
	return SCHOOL_PROFILE_BY_KEY[normalizeSchoolName(name)]
}
